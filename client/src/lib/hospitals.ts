export const getTypeAr = (type: string): string => {
  const types: Record<string, string> = {
    hospital: "مستشفى",
    health_center: "مركز صحي",
    clinic: "عيادة",
    pharmacy: "صيدلية",
  };
  return types[type] || type;
};

export const getTypeColor = (type: string): string => {
  const colors: Record<string, string> = {
    hospital: "bg-red-600 text-white",
    health_center: "bg-blue-600 text-white",
    clinic: "bg-green-600 text-white",
    pharmacy: "bg-purple-600 text-white",
  };
  return colors[type] || "bg-gray-600 text-white";
};

